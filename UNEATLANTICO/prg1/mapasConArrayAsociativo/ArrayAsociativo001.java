	static void imprimeMapa(String[] castillo) {

		for (int i = 0; i < castillo.length; i++) {
			for (int j = 0; j < castillo[i].length(); j++) {
				System.out.print(mapea(castillo[i].charAt(j)));
			}
			System.out.println();
		}
	}

	static String mapea(char elemento) {

		if (elemento == ' ') {
			return "   ";
		} else if (elemento == '.') {
			return " . ";
		} else if (elemento == '-') {
			return "[#]";
		} else if (elemento == '=') {
			return "|||";
		} else if (elemento == '|') {
			return "[#]";
		} else if (elemento == ':') {
			return "oOo";
		} else if (elemento == '+') {
			return "...";
		} else if (elemento == 'O') {
			return "[ ]";
		} else if (elemento == '#') {
			return ".:.";
		} else if (elemento == '*') {
			return "***";
		} else if (elemento == '$') {
			return "$$$";
		} else if (elemento == 'X') {
			return "XXX";
		} else if (elemento == '%') {
			return "%%%";
		}
	
		return "???";
	}
